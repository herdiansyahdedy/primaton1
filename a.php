<h2>Langkah 3#: Menjaga postur tubuh saat bekerja</h2>
<p>Poin penting lainnya yang sering disepelekan oleh pekerja untuk mengelola pola hidup sehat adalah menjaga postur tubuh, terutama posisi duduk, untuk tetap ideal saat bekerja. Dalam satu hari, seorang pekerja bisa menghabiskan waktu berjam-jam bekerja dalam posisi duduk, entah itu di depan layar komputer, rapat, presentasi, ataupun bertemu dengan klien. Membiarkan diri berlama-lama dalam posisi duduk tentu merugikan bagi kesehatan dan berarti berlawanan dengan pola hidup sehat. Duduk terlalu lama dengan kepala menghadap ke arah komputer dapat membebani leher dan tulang belakang kita. Selain itu, duduk terlalu lama juga berisiko meningkatkan potensi penyakit jantung, diabetes, serta kanker.</p>
<p>Untuk itu, simak dan ikuti tips yang dirangkum dari laman <a href="https://www.greenfieldhealth.com/your-health/workplace-ergonomics-the-ideal-posture-for-sitting-at-your-desk/">greenfieldhealth</a> berikut untuk mendapatkan postur duduk yang lebih ideal dan demi pola hidup yang lebih sehat di tempat kerja.</p>
<ul>
<li>Duduklah di kursi dengan kaki menapak pada lantai. Selanjutnya, pastikan jika punggung kursi memiliki ukuran yang cukup tinggi, menjangkau punggung hingga bagian atas tulang belikat. Ukuran kursi yang baik juga seharusnya memberikan jarak yang cukup di antara bagian belakang lutut dan kursi.</li>
<li>Tekuk siku 90 derajat dan pastikan sandaran lengan memberikan tekanan lembut pada lengan bagian bawah.</li>
<li>Posisikan keyboard berada di dekat area yang nyaman bagi bahu ,lengan, serta pergelangan tangan untuk bergerak. Letakkan mouse di dekat keyboard.</li>
<li>Monitor ditempatkan dengan jarak sepanjang jarak lengan dari tubuh dan posisi ujung atas monitor berada pada <em>eye-level</em> pekerja.</li>
</ul>
<h2>Langkah 4#: Bergerak sebanyak mungkin</h2>
<p>Terkadang pekerjaan kantor memaksa pekerja untuk terus duduk di depan layar komputer. Hal itu tentu berlawanan dengan pola hidup sehat dan berdampak negatif terhadap kesehatan tubuh, seperti tulang punggung atau mata. Berdasarkan hal tersebut, maka pekerja perlu menyediakan waktu untuk menggerakkan tubuhnya. Berjalan dari tempat parkir ataupun tempat pemberhentian kendaraan umum bisa menjadi langkah awal yang baik untuk memperbanyak gerak. Langkah berikutnya, coba pilih untuk naik tangga ketimbang eskalator atau lift untuk sampai di tempat kamu bekerja. Jika kantor kamu berada di lantai yang sangat tinggi, kamu bisa berhenti dari tiga sampai lima lantai sebelum tempatmu bekerja, lalu melanjutkan perjalanan dengan menempuh tangga.</p>
<h2>Langkah 5#: Olahraga ringan di tempat kerja</h2>
<p>Pola hidup sehat tentu belum lengkap tanpa adanya olahraga. Bagi kamu yang bekerja di kantor yang memberikan ruang dan waktu untuk berolahraga, alangkah baiknya kamu menggunakan fasilitas tersebut semaksimal mungkin. Namun jika kantor kamu tidak menyediakan hal tersebut, kamu bisa tetap melakukan olahraga ringan yang dapat dilakukan tanpa meninggalkan ruang kerjamu.</p>
<p>Mulailah dengan melakukan pemanasan, kemudian lanjutkan dengan menggunakan alat bantu yang ringkas seperti barbel kecil. Selain itu, selama duduk di ruang kerja, sesekali angkat kaki kamu secara bersamaan hingga sejajar dengan paha, tahan selama 10 detik, dan turunkan. Ulangi gerakan tersebut dengan beberapa repetisi sesuai kemampuanmu.</p>
