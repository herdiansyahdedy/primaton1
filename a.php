<h2>Langkah 6#: Awasi pola hidup sehat setelah jam kerja</h2>
<p>Penerapan pola hidup sehat selama bekerja tidak ada artinya jika kamu tidak menjaganya saat selepas jam kerja. Pola hidup sehat tidak berarti kamu dilarang bersosialisasi. Kamu hanya perlu mengontrol diri agar pola hidup sehat yang telah dilaksanakan di kantor menjadi sia-sia dengan gaya hidup yang buruk di luar kantor. Pilih kegiatan bersosialisasi yang turut mendukung pola hidup sehatmu, misalnya dengan pergi ke <em>gym</em> atau <em>jogging</em> di taman. Pastikan juga untuk tetap mengawasi asupan makanan selama bersosialisasi setelah jam kerja.</p>
<p>Sejatinya, pola hidup sehat dimulai dari diri sendiri. Sesibuk apapun kamu, jangan sampai kesibukan tersebut menghalangimu untuk tetap memiliki tubuh yang bugar. Dengan tubuh yang sehat, tentu aliran darah ke otak menjadi lebih lancar, meningkatkan fokus dan konsentrasi secara optimal, hingga memancing ide-ide segar agar lebih mudah timbul diimplementasikan dalam pekerjaan. Dengan tubuh dan pola hidup sehat, niscaya kamu mampu bekerja secara maksimal. Baca juga artikel terkait tips-tips pola hidup sehat lainnya dalam dunia kerja di <a href="https://glints.com/id/lowongan/">Glints Blog</a>.</p>
