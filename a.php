<p>Namun, kesulitan-kesulitan tersebut bisa ditanggulangi dengan merancang pola hidup sehat yang praktis dan mudah dilakukan. Memulai pola hidup sehat bisa diawali dari hal-hal yang amat sederhana namun tidak jarang dilupakan oleh sebagian besar pekerja. Untuk itu, cermati langkah-langkah pola hidup sehat berikut berikut dan segera terapkan dalam keseharianmu!</p>
<h2>Langkah 1#: Perhatikan pola makan</h2>
<p>Pola hidup sehat dalam ruang kerja dimulai dari mengontrol asupan makanan yang dikonsumsi. Aktivitas kerja dan proses berpikir dapat memicu rasa lapar lebih cepat. Untuk menjaga pola hidup tetap sehat, penting bagi pekerja untuk menyimpan camilan sehat di meja kerjanya demi mencegah asupan kalori yang tidak dibutuhkan. Beberapa camilan sehat yang dapat disimpan untuk menunjang pola hidup sehat adalah buah segar dan kacang-kacangan.</p>
<p>Kamu juga bisa membawa bekal makanan yang dimasak dari rumah untuk dikonsumsi saat makan siang di kantor. Kontrol asupan makanan yang dimasak sendiri jelas lebih terjaga kualitasnya untuk mendukung pola hidup sehat ketimbang membeli makan siang di luar, baik dari segi kebersihan maupun nutrisinya. Dada ayam rebus, ikan, nasi merah, roti gandum utuh, sayur, dan lalapan dapat menjadi opsi menu yang baik bagi pola hidup sehat.</p>
<p>Hindari makanan tidak sehat selama kerja ataupun saat jam makan siang, seperti keripik kentang,<em> junk food</em>, ataupun makanan instan. Sempurnakan makan siang dan pola hidup sehat kamu dengan buah atau yoghurt.</p>
