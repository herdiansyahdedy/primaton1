<h2>Langkah 2#: <em>Stay hydrated!</em></h2>
<p>Jika kamu ingin menjalani pola hidup sehat, apakah kamu sudah cukup banyak minum air putih saat berada di kantor? Pertanyaan sesederhana ini seringkali terlewat bagi para pekerja yang ingin tetap menjaga pola hidup sehat. Tidak sedikit pekerja yang cenderung untuk tidak memikirkan asupan cairan ke dalam tubuh saat berada di kantor. Pasalnya, ruangan kantor dengan paparan suhu AC yang dingin dianggap menghilangkan rasa haus. Padahal yang luput dari kesadaran pekerja adalah bahwa proses berpikir dan aktivitas kerja juga membuang asupan cairan di dalam tubuh, sehingga secara tidak sadar menyebabkan pekerja dehidrasi. Kondisi dehidrasi pada tubuh bisa mengakibatkan kurangnya konsentrasi, lesu, kembung, hingga menurunkan tingkat kewaspadaan dalam bekerja.</p>
<p>Oleh karena itu, botol minum, gelas, atau mug, bisa menjadi teman yang baik untuk menjalani pola hidup sehat. Setidaknya, pekerja membutuhkan 3 &#8211; 5 gelas air putih sehari saat bekerja untuk menjaga kesehatan tubuh. Selain itu, air putih juga efektif bagi kamu yang saat ini sedang mendalami pola hidup sehat, terutama untuk menenangkan diri dari tekanan kantor, menghilangkan rasa kantuk, dan meningkatkan fungsi ginjal dan hati. Jika kamu bekerja di luar ruangan, banyak menggunakan fisik, dan di bawah paparan sinar matahari yang terik, kamu perlu untuk lebih banyak mengonsumsi air putih hingga 2,8 liter per hari untuk menjaga pola hidup sehat.</p>
